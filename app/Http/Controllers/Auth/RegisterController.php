    {
        $shopifyClient = new ShopifyClient([
            'private_app' => true,
            'api_key'     => '5f7733f908091777118b5e4791fd7acf',
            'password'    => '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',
            'shop'        => 'bem-bonda.myshopify.com',
